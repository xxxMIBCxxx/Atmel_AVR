#ifndef SDLIB_H_
#define SDLIB_H_

#include <avr/io.h>
#include <stdbool.h>


#define SdCS_HI				( PORTB = PORTB | SD_CS )
#define SdCS_LOW			( PORTB = PORTB & ~(SD_CS) )
#define SD_BLOCK_SIZE		( 512 )

//-------------------------------------------------------------------------------------------------
// SD初期化処理
//-------------------------------------------------------------------------------------------------
bool SD_Init();

//-------------------------------------------------------------------------------------------------
// ブロック長を設定
//-------------------------------------------------------------------------------------------------
bool SD_SetBlockSize( uint32_t uiBlockSize );

//-------------------------------------------------------------------------------------------------
// データ読込処理
//-------------------------------------------------------------------------------------------------
bool SD_ReadBlockData( uint32_t uiAddress, unsigned char *pReadData );

//-------------------------------------------------------------------------------------------------
// データ書込み処理
//-------------------------------------------------------------------------------------------------
bool SD_WriteBlockData( uint32_t uiAddress, unsigned char *pWriteData );

#endif /* SDLIB_H_ */