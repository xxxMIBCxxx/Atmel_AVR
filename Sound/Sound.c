#define F_CPU			(8000000L)
#include <avr/io.h>
#include <util/delay.h>

//---< 音階 >---------------------------------------------------------------------------------------------------
//								  ド,  ド#,  レ,   レ#,   ミ, ファ,ファ#,   ソ,  ソ#,   ラ,   シ,  シ#
const unsigned int	SCALE[] = {  262,  277,  293,  311,  330,  349,  370,  392,  415,  440,  466,  494, 
                                 524,  554,  586,  622,	 660,  698,  740,  784,  830,  880,  932,  988, 
								1048, 1108, 1172, 1244, 1320, 1396, 1480, 1568, 1660, 1760, 1864, 1976,
								2096, 2216, 2344, 2488, 2640, 2792, 2960, 3136, 3320, 3520, 3728, 3952, 
								4192, 4432, 4688, 4976, 5280, 5584, 5920, 6272, 6640, 7040, 7456, 7904,
								8384, 8864, 9376, 9952,10560,11168,11840,12544,13280,14080,14912,15808 };
//--------------------------------------------------------------------------------------------------------------

int main(void)
{
	DDRD   = 0b00100000;
	TCCR1A = 0b01000011;		// 一致でOC1Aピン トグル出力,高速PWM動作
	TCCR1B = 0b00011001;		// clkI/O(前置分周なし)
	int				i = 0;
	unsigned int	temp = 0;


    while(1)
    {
		temp = (F_CPU / SCALE[i]);
		OCR1A = temp;
		_delay_ms( 1000 );
		i++;
		if ( i >= 49 )
		{
			i = 0;
		}		
    }
	
	return 0;
}